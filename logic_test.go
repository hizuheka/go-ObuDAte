package main

import "testing"

func TestReplaceTime(t *testing.T) {
	tests := []struct {
		name         string
		input        string
		wantStr      string
		wantReplaced bool
	}{
		// 正常系・仕様通りの変換
		{"通常の1日加算と00時", `"2000-01-01","24:"`, `"2000-01-02","00:"`, true},
		{"通常の1日加算と01時", `"2000-01-02","25:"`, `"2000-01-03","01:"`, true},
		{"通常の1日加算と02時", `"2000-01-03","26:"`, `"2000-01-04","02:"`, true},
		{"通常の1日加算と03時", `"2000-01-04","27:"`, `"2000-01-05","03:"`, true},
		{"通常の1日加算と04時", `"2000-01-05","28:"`, `"2000-01-06","04:"`, true},
		{"通常の1日加算と05時", `"2000-01-06","29:"`, `"2000-01-07","05:"`, true},
		{"通常の1日加算と06時", `"2000-01-01","30:"`, `"2000-01-02","06:"`, true},
		{"通常の1日加算と07時", `"2000-01-01","31:"`, `"2000-01-02","07:"`, true},
		{"通常の1日加算と08時", `"2000-01-01","32:"`, `"2000-01-02","08:"`, true},
		{"通常の1日加算と09時", `"2000-01-01","33:"`, `"2000-01-02","09:"`, true},
		{"通常の1日加算と10時", `"2000-01-01","34:"`, `"2000-01-02","10:"`, true},
		{"通常の1日加算と11時", `"2000-01-01","35:"`, `"2000-01-02","11:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","36:"`, `"2000-01-02","12:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","37:"`, `"2000-01-02","13:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","38:"`, `"2000-01-02","14:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","39:"`, `"2000-01-02","15:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","40:"`, `"2000-01-02","16:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","41:"`, `"2000-01-02","17:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","42:"`, `"2000-01-02","18:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","43:"`, `"2000-01-02","19:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","44:"`, `"2000-01-02","20:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","45:"`, `"2000-01-02","21:"`, true},
		{"通常の1日加算と12時", `"2000-01-01","46:"`, `"2000-01-02","22:"`, true},
		{"通常の1日加算と23時", `"2000-01-31","47:"`, `"2000-02-01","23:"`, true},

		// 日付計算のエッジケース
		{"年末の繰り上がり", `"2023-12-31","24:"`, `"2024-01-01","00:"`, true},
		{"平年の2月28日", `"2023-02-28","25:"`, `"2023-03-01","01:"`, true},
		{"閏年の2月28日", `"2024-02-28","25:"`, `"2024-02-29","01:"`, true},
		{"閏年の2月29日", `"2024-02-29","25:"`, `"2024-03-01","01:"`, true},

		// 行内に他の要素が含まれるケース
		{"行の一部に含まれる場合", `1,"2000-01-01","24:",abc`, `1,"2000-01-02","00:",abc`, true},

		// 対象外のケース（置換されないこと）
		{"対象外（時間が23時以下）", `"2000-01-01","23:"`, `"2000-01-01","23:"`, false},
		{"対象外（時間が48時以上）", `"2000-01-01","48:"`, `"2000-01-01","48:"`, false},

		// 異常系（不正なデータ）
		{"不正な日付フォーマット", `"2023-13-45","24:"`, `"2023-13-45","24:"`, false},
		{"カンマがない等フォーマット不一致", `"2000-01-01" "24:"`, `"2000-01-01" "24:"`, false},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			gotStr, gotReplaced := ReplaceTime(tt.input)
			if gotStr != tt.wantStr {
				t.Errorf("got string = %v, want %v", gotStr, tt.wantStr)
			}
			if gotReplaced != tt.wantReplaced {
				t.Errorf("got replaced = %v, want %v", gotReplaced, tt.wantReplaced)
			}
		})
	}
}
